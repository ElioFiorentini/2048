void	print_char(t_font *font, int x, int y, int c, int size)
{
	if (size <= 0)
		return ;
	const int WIDTH = FONT_WIDTH * size;
	for (int i = 0; i < FONT_HEIGHT; i++)
		for (int j = 0; j < WIDTH; j++)
			if (font->chars[c][i][j / size] == '#')
				mvaddch(y + i, x + j, ' ');
}

static void	print_digits(char digits[10][5][4], int x, int y, int nb, int size)
{
	if (nb <= 0)
		return ;
	int dgt = nb % 10;
	print_digits(digits, x - (3 * size + 1), y, nb / 10, size);
	for (int i = 0; i < 5; i++)
		for (int j = 0; j < 3 * size; j++)
			if (digits[dgt][i][j / size] == '#')
				mvaddch(y + i, x + j, ' ');
}


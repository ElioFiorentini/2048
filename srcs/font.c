void	print(t_font *font, int x, int y, char *str, int size)
{
	if (!str)
		return ;
	const int STEP = FONT_WIDTH * size;
	int	offset = 0;
	for (size_t i = 0; str[i] != '\0'; i++)
	{
		print_char(font, x + offset, y, str[i], size);
		offset += STEP;
	}
}
